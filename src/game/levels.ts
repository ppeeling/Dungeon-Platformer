export function generateLevel(levelIndex: number): string[] {
  const numChunks = 3 + Math.min(levelIndex, 15);
  const height = 15;
  const level: string[] = Array(height).fill("");

  let availableChunks = [...CHUNKS];
  if (levelIndex >= 15) {
    availableChunks = availableChunks.concat(LADDER_CHUNKS);
  }
  if (levelIndex >= 5) {
    // Wall chunks are already in CHUNKS
  }
  if (levelIndex >= 8) {
    availableChunks = availableChunks.concat(HAZARD_CHUNKS);
  }

  // Start chunk
  const startChunk = [
    "..........",
    "..........",
    "..........",
    "..........",
    "..........",
    "..........",
    "..........",
    "..........",
    "..........",
    "..........",
    "..........",
    "..........",
    "P.........",
    "##########",
    "##########"
  ];

  for (let i = 0; i < height; i++) {
    level[i] += startChunk[i];
  }

  const isBossLevel = levelIndex === 4; // 5th level
  const isBlueBossLevel = levelIndex === 9; // 10th level
  const isGreenBossLevel = levelIndex === 14; // 15th level
  const isFinalLevel = levelIndex === 19; // 20th level
  const isSecretLevel = levelIndex === 20; // 21st level
  let keyPlaced = isBossLevel || isBlueBossLevel || isGreenBossLevel || isFinalLevel || isSecretLevel;

  if (isSecretLevel) {
    // Secret level is just a big empty space with some laddered platforms
    const secretLevel: string[] = Array(height).fill("");
    const secretWidth = 100;
    
    for (let i = 0; i < height; i++) {
      let row = "";
      if (i === 0 || i === height - 1) {
        row = "#".repeat(secretWidth);
      } else if (i === height - 2) {
        row = "#".repeat(secretWidth);
      } else if (i === height - 3) {
        // Floor level
        row = "P" + ".".repeat(secretWidth - 2) + "#";
      } else {
        row = "#" + ".".repeat(secretWidth - 2) + "#";
      }
      secretLevel[i] = row;
    }

    // Add some laddered platforms
    const platformPositions = [20, 40, 60, 80];
    for (const x of platformPositions) {
      // Platform at height 8
      for (let px = x - 2; px <= x + 2; px++) {
        let row = secretLevel[8].split('');
        if (px === x) {
          row[px] = '='; // Make ladder go through platform
        } else {
          row[px] = '#';
        }
        secretLevel[8] = row.join('');
      }
      // Ladder from floor to platform
      for (let ly = 9; ly <= 11; ly++) {
        let row = secretLevel[ly].split('');
        row[x] = '=';
        secretLevel[ly] = row.join('');
      }
    }

    // Place the white boss
    let row = secretLevel[12].split('');
    row[50] = 'V'; // Using 'V' for White Boss
    secretLevel[12] = row.join('');

    // Add heart tokens and power ups
    const tokenPositions = [10, 20, 30, 40, 60, 70, 80];
    const tokens = ['H', 'H', 'U', 'H', 'H', 'J', 'H'];
    for (let i = 0; i < tokenPositions.length; i++) {
      let tokenRow = secretLevel[12].split('');
      tokenRow[tokenPositions[i]] = tokens[i];
      secretLevel[12] = tokenRow.join('');
    }

    // Place the door at the end
    let doorRow = secretLevel[12].split('');
    doorRow[secretWidth - 5] = 'D';
    secretLevel[12] = doorRow.join('');

    return secretLevel;
  }

  for (let c = 0; c < numChunks; c++) {
    const chunkIndex = Math.floor(Math.random() * availableChunks.length);
    const chunk = availableChunks[chunkIndex].map(row => row.split(''));

    // Replace some monsters with green monsters in later levels
    if (levelIndex >= 10 && levelIndex < 15) {
      for (let y = 0; y < height; y++) {
        for (let x = 0; x < chunk[y].length; x++) {
          if (chunk[y][x] === 'M' && Math.random() < 0.7) {
            chunk[y][x] = 'G'; // Green monster
          }
        }
      }
    }

    // Replace some monsters with yellow monsters in level 16+
    if (levelIndex >= 15) {
      for (let y = 0; y < height; y++) {
        for (let x = 0; x < chunk[y].length; x++) {
          if (chunk[y][x] === 'M' && Math.random() < 0.8) {
            chunk[y][x] = 'Y'; // Yellow monster
          } else if (chunk[y][x] === 'M' && Math.random() < 0.5) {
            chunk[y][x] = 'G'; // Green monster
          }
        }
      }
    }
    
    // Randomly place key in one of the chunks
    if (!keyPlaced && Math.random() < (1 / (numChunks - c))) {
      // Find a valid spot for key
      for (let y = height - 3; y >= 5; y--) {
        for (let x = 2; x < 8; x++) {
          if (chunk[y][x] === '.' && chunk[y+1][x] === '#') {
            chunk[y][x] = 'K';
            keyPlaced = true;
            break;
          }
        }
        if (keyPlaced) break;
      }
    }

    // Randomly place power-ups
    if (Math.random() < 0.4) { // 40% chance per chunk
      const powerups = ['U', 'J', 'I', 'H'];
      const p = powerups[Math.floor(Math.random() * powerups.length)];
      let placed = false;
      for (let y = height - 3; y >= 5; y--) {
        for (let x = 2; x < 8; x++) {
          if (chunk[y][x] === '.' && chunk[y+1][x] === '#') {
            chunk[y][x] = p;
            placed = true;
            break;
          }
        }
        if (placed) break;
      }
    }

    for (let i = 0; i < height; i++) {
      level[i] += chunk[i].join('');
    }
  }

  // End chunk
  let endChunk: string[];

  if (isFinalLevel) {
    endChunk = [
      "............................................................",
      "............................................................",
      "............................................................",
      "............................................................",
      "............................................................",
      ".........................=..................................",
      ".........................=..................................",
      ".........................=..................................",
      ".........................=..................................",
      ".........................=..................................",
      ".........................=..................................",
      ".........................W...............................D..",
      ".........................=...............................#..",
      "############################################################",
      "############################################################"
    ];
  } else if (isGreenBossLevel) {
    endChunk = [
      "............................................................",
      "............................................................",
      "............................................................",
      "............................................................",
      "............................................................",
      "............................................................",
      "............................................................",
      "............................................................",
      "............................................................",
      "............................................................",
      "............................................................",
      ".........................X...............................D..",
      ".........................................................#..",
      "############################################################",
      "############################################################"
    ];
  } else if (isBlueBossLevel) {
    endChunk = [
      "............................................................",
      "............................................................",
      "............................................................",
      ".......................................Z....................",
      "............................................................",
      "......=..........=..........=..........=....................",
      "....###=.......###=.......###=.......###=...................",
      "......=..........=..........=..........=....................",
      "......=..........=..........=..........=....................",
      "......=..........=..........=..........=....................",
      "......=..........=..........=..........=....................",
      "......=..H.......=..H.......=..H.......=..H..............D..",
      "......=..........=..........=..........=.................#..",
      "############################################################",
      "############################################################"
    ];
  } else if (isBossLevel) {
    endChunk = [
      "........................................",
      "........................................",
      "........................................",
      "........................................",
      "........................................",
      "........................................",
      "........................................",
      "........................................",
      "........................................",
      "........................................",
      ".........................B...........D..",
      ".....................................#..",
      "########################################",
      "########################################",
      "########################################"
    ];
  } else {
    endChunk = [
      "..........",
      "..........",
      "..........",
      "..........",
      "..........",
      "..........",
      "..........",
      "..........",
      "..........",
      "..........",
      ".......D..",
      ".......#..",
      "##########",
      "##########",
      "##########"
    ];
    if (!keyPlaced) {
      endChunk[11] = "....K..D..";
    }
  }

  for (let i = 0; i < height; i++) {
    level[i] += endChunk[i];
  }

  // Add borders
  const width = level[0].length;
  for (let i = 0; i < height; i++) {
    level[i] = '#' + level[i] + '#';
  }
  level[0] = '#'.repeat(width + 2);
  level[height - 1] = '#'.repeat(width + 2);

  // For levels 6-10 (indices 5-9), randomly replace some M with F (Flying Monster)
  if (levelIndex >= 5) {
    for (let y = 0; y < height; y++) {
      let row = level[y].split('');
      for (let x = 0; x < row.length; x++) {
        if (row[x] === 'M' && Math.random() < 0.5) {
          row[x] = 'F';
        } else if (row[x] === '.' && Math.random() < 0.005 && y < 8 && y > 2) {
          // Also spawn some flying monsters in the air
          row[x] = 'F';
        }
      }
      level[y] = row.join('');
    }
  }

  // Randomly add powerups by replacing some coins
  for (let y = 0; y < height; y++) {
    let row = level[y].split('');
    for (let x = 0; x < row.length; x++) {
      if (row[x] === 'C' && Math.random() < 0.05) {
        const r = Math.random();
        if (r < 0.33) row[x] = 'U'; // Speed
        else if (r < 0.66) row[x] = 'J'; // Jump
        else row[x] = 'I'; // Invincibility
      }
    }
    level[y] = row.join('');
  }

  return level;
}
